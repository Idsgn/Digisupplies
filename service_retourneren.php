<!-- Klantenservices -->

<h1>Retourneren</h1>
<p>Je bestelling of een deel ervan retour sturen is geen enkel probleem bij ons!</p>

<h2>Algemene ruil-/ retourneertermijn</h2>
<p>Indien je in het bezit bent van de originele factuur kan je artikelen, die je via onze webwinkel hebt gekocht, binnen 14 dagen na ontvangst ruilen en retourneren. Indien gewenst ontvang je  het aankoopbedrag terug. Het artikel dient wel in de zo oorspronkelijk mogelijke verpakking, indien redelijkerwijs te zitten en in ongebruikte en verkoopbare staat te zijn.</p>

<h2>Defecte producten</h2>
<p>Wanneer je een kapot product heeft ontvangen, wordt deze door ons kosteloos omgeruild voor exact hetzelfde product, indien de aankoop niet is gebruikt en in de originele verpakking verkeert.</p>

<h2>Producten retour sturen</h2>
<p>Indien je artikelen retour wilt sturen, zien we graag dat je van tevoren contact opneemt met onze klantenservice via <a href="mailto: info@digisupplies.nl" title="E-mail info@digisupplies.nl">info@digisupplies.nl</a>. Graag altijd het factuurnummer vermelden.</p>
<p>Stuur altijd je artikelen retour via het postkantoor, dan ontvang je een verzendbewijs en is het mogelijk om de retourzending na te zoeken. Digisupplies.nl neemt anders geen verantwoording op zich indien het pakket zoek is geraakt.</p>
<p>Het terug sturen van producten is uw verantwoordelijkheid totdat de postbezorger het heeft afgeleverd bij ons. Het is ten zeerste aan te raden om dit aangetekend te versturen met een track & trace code, zodat u een bewijs heeft van het retour sturen.</p>
<p>Retouradres: Digisupplies.nl, Postbus 465, 2740 AL te Waddinxveen. Nadat wij je pakket retour hebben ontvangen nemen wij binnen 10 werkdagen contact met je op via de email.</p>

<h2>Klachtenprocedure</h2>
<p>Indien je een klacht hebt over een bij ons gekocht product dan zien we graag dat je eerst contact met ons opneemt per email.</p>
<p>Een andere klacht (klachten met betrekking tot anders dan het product) ingediend bij ons per email zullen wij binnen uiterlijk 10 dagen verwerken.</p>

